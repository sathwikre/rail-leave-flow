export function normalizeDesignation(value) {
  const compact = String(value ?? "").trim().toUpperCase().replace(/\s+/g, " ");
  const key = compact.replace(/[./\s-]/g, "");
  const normalized = {
    DYSS: "DY SS",
    PMAN: "P/MAN",
    PWOMAN: "P/WOMAN",
    SMR: "SMR",
    SM: "SM",
    SS: "SS",
    APM: "APM",
    SMASTER: "S/MASTER",
    CTNC: "CTNC",
    SRCLERK: "SR.CLERK",
    SHGMASTER: "SHG-MASTER",
  };
  return normalized[key] ?? compact;
}

export function parseEmployeeDate(value) {
  const raw = String(value ?? "").trim();
  if (!raw) return undefined;
  const match = raw.match(/^(\d{1,2})[-/.](\d{1,2})[-/.](\d{2}|\d{4})$/);
  if (!match) return undefined;
  const [, day, month, rawYear] = match;
  const dayNumber = Number(day);
  const monthNumber = Number(month);
  if (dayNumber < 1 || dayNumber > 31 || monthNumber < 1 || monthNumber > 12) return undefined;
  const yearNumber = Number(rawYear);
  const year = rawYear.length === 2
    ? String(yearNumber <= 30 ? 2000 + yearNumber : 1900 + yearNumber)
    : rawYear;
  const date = new Date(
    year + "-" + String(monthNumber).padStart(2, "0") + "-" + String(dayNumber).padStart(2, "0") + "T00:00:00.000Z",
  );
  return Number.isNaN(date.getTime()) ? undefined : date;
}

export function employeeDocument(record) {
  return {
    employeeId: String(record.employeeId).trim(),
    name: String(record.name).trim(),
    designation: normalizeDesignation(record.designation),
    stationName: String(record.stationName).trim().toUpperCase(),
    dob: parseEmployeeDate(record.dob),
    doj: parseEmployeeDate(record.doj),
    doa: parseEmployeeDate(record.doa),
    phone: String(record.phone ?? "").trim(),
  };
}
